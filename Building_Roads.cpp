// Modular arithmetic
int mod_add(int a, int b, int m = MOD) { return ((a % m + b % m + m) % m); }
int mod_sub(int a, int b, int m = MOD) { return ((a % m - b % m + m) % m); }
int mod_mul(int a, int b, int m = MOD) { return ((a % m) * (b % m)) % m; }
int mod_pow(int a, int b, int m = MOD) {
    int res = 1;
    a %= m;
    while (b > 0) {
        if (b & 1) res = mod_mul(res, a, m);
        a = mod_mul(a, a, m);
        b >>= 1;
    }
    return res;
}
int mod_inv(int a, int m = MOD) {
    return mod_pow(a, m - 2, m);
}

struct DSU {
    vector<int> parent;
    vector<int> size;
    
    void init(int n) {
        parent.assign(n, 0);
        iota(parent.begin(), parent.end(), 0);
        size.assign(n, 1);
    }

    int find(int v) {
        if (v == parent[v]) return v;
        return parent[v] = find(parent[v]);
    }

    bool merge(int a, int b) {
        a = find(a);
        b = find(b);
        if (a == b) return false;
        if (size[a] < size[b]) swap(a, b); // Corrected: union by size
        parent[b] = a;
        size[a] += size[b];
        return true;
    }
};

void solve() {
    int n, m;
    cin >> n >> m;
    DSU d;
    d.init(n);

    while (m--) {
        int a, b;
        cin >> a >> b;
        d.merge(a, b);
    }

    vector<int> path;
    for (int i = 1; i <= n - 1; i++) {
        if (d.find(i) != d.find(i + 1)) {
            d.merge(i, i + 1); // Important: merge them now
            path.pb(i);
            path.pb(i + 1);
        }
    }

    cout << path.size() / 2 << endl;
    for (int i = 0; i < path.size(); i += 2) {
        cout << path[i] << " " << path[i + 1] << endl;
    }
}

